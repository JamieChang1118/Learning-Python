r = 7
pi = 3.14159
area = r * r * pi

print("半徑=" + str(r) + " 圓周率=" + str(pi) + " 計算" + area + "=" + str(r*r*pi))

print("半徑={} 圓周率={} 計算{}={}".format(r , pi , area , r*r*pi))

print("半徑=%d 圓周率=%.2f 計算%s=%.2f" %(r , pi , area , r*r*pi))

print("半徑=%08d 圓周率=%08.2f 計算%s=%08.2f" %(r , pi , area , r*r*pi))

